package lesson_03;

public class ArfOperators {
    public static void main(String[] args) {

        int a = 20;
        int b = 7;

        double c = 20;
        double d = 7;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));

        System.out.println();

        System.out.println("c / d = " + (c / d));
        System.out.println("c % d = " + (c % d));

        a++;
        System.out.println("a = " + a);
        a++;
        System.out.println("a = " + a);
        a++;
        System.out.println("a = " + a);
        a++;
        System.out.println("a = " + a);
    }
}
